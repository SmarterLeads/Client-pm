export type ConversionBreakdownPdfRow = {
  id: string;
  label: string;
  conversionType: string;
  current: number;
  prior: number;
};

/** One tab’s worth of content (Overview or a single platform). */
export type ReportPdfSection = {
  slug: string;
  /** e.g. "Overview" / "Google Ads" */
  viewTitle: string;
  hero: KpiStat[];
  conversionRows: ConversionBreakdownPdfRow[];
  channelRows: ChannelSummary[] | null;
  campaignRows: CampaignTableRow[] | null;
  /** Meta tab only — column visibility from client_metric_config. */
  metaCampaignColumnVisibility?: Record<string, boolean>;
  /** Meta ecommerce — Purchases / Purchase Value / ROAS columns. */
  useMetaEcommerceColumns?: boolean;
  /** Rows match the chart dataset (daily Performance Trends series). Split into chunks for pagination. */
  performanceChunks: ReportSeriesPoint[][];
  ytdRows: YtdMonthTableRow[];
};

export type ReportPdfBundle = {
  agencyName: string;
  agencyLogoUrl: string | null;
  agencyPrimaryColor: string;
  clientName: string;
  dateRangeLabel: string;
  generatedAtLabel: string;
  /** Overview first, then each platform tab in product order. */
  sections: ReportPdfSection[];
};

type PdfHeaderProps = {
  agencyLogoUrl: string | null;
  clientName: string;
  viewTitle: string;
  dateRangeLabel: string;
};

export function ReportPdfDocument({ bundle }: { bundle: ReportPdfBundle }) {
  const styles = buildStyles(bundle.agencyPrimaryColor);
  const primary = bundle.agencyPrimaryColor;
  const footer = (
    <View style={styles.footer}>
      <Text style={styles.subtle}>Generated by {bundle.agencyName}</Text>
      <Text style={styles.subtle}>{bundle.generatedAtLabel}</Text>
    </View>
  );

  const sectionCount = bundle.sections.length;

  return (
    <Document>
      {bundle.sections.flatMap((section, sectionIndex) => {
        const dailyChunks = section.performanceChunks ?? [];
        const firstDaily = dailyChunks[0];
        const restDaily = dailyChunks.slice(1);
        const hasPerfTrends = dailyChunks.some((c) => c.length > 0);
        const isLastSection = sectionIndex === sectionCount - 1;
        const headerProps: PdfHeaderProps = {
          agencyLogoUrl: bundle.agencyLogoUrl,
          clientName: bundle.clientName,
          viewTitle: section.viewTitle,
          dateRangeLabel: bundle.dateRangeLabel,
        };

        return [
          <Page key={`${section.slug}-main`} size="LETTER" style={styles.page}>
            <PdfHeader header={headerProps} styles={styles} />
            <HeroSection hero={section.hero} primaryColor={primary} styles={styles} />
            {hasPerfTrends ? (
              <>
                <PdfSectionTitle styles={styles}>Performance Trends</PdfSectionTitle>
                <View style={styles.surfaceCard}>
                  {firstDaily != null && firstDaily.length ? (
                    <PerfTableChunk rows={firstDaily} styles={styles} />
                  ) : (
                    <Text style={styles.muted}>No daily performance rows for this selection.</Text>
                  )}
                </View>
              </>
            ) : null}
            {section.conversionRows.length > 0 ? (
              <>
                <PdfSectionTitle styles={styles}>Conversion Breakdown</PdfSectionTitle>
                <ConversionBreakdownCards
                  rows={section.conversionRows}
                  styles={styles}
                  primaryColor={primary}
                />
              </>
            ) : null}
            {section.channelRows != null && section.channelRows.length > 0 ? (
              <>
                <PdfSectionTitle styles={styles}>Channel Breakdown</PdfSectionTitle>
                <ChannelBreakdownCards rows={section.channelRows} styles={styles} />
              </>
            ) : null}
            {section.campaignRows != null ? (
              <>
                <CampaignSectionTitle styles={styles}>Campaign performance</CampaignSectionTitle>
                <CampaignTable
                  rows={section.campaignRows}
                  styles={styles}
                  useMetaReachColumns={canonicalReportPlatformSlug(section.slug) === "meta"}
                  useEcommerceColumns={section.useMetaEcommerceColumns === true}
                  columnVisibility={
                    section.useMetaEcommerceColumns ? undefined : section.metaCampaignColumnVisibility
                  }
                />
              </>
            ) : null}
          </Page>,
          ...(hasPerfTrends
            ? restDaily.map((chunk, idx) => (
                <Page key={`${section.slug}-daily-${idx}`} size="LETTER" style={styles.page}>
                  <PdfHeader header={headerProps} styles={styles} minimal />
                  <Text style={[styles.subtle, { marginBottom: 10 }]}>{bundle.dateRangeLabel}</Text>
                  <Text style={[styles.sectionTitleCenter, { marginTop: 0, marginBottom: 8 }]}>
                    Performance Trends (continued)
                  </Text>
                  <View style={styles.surfaceCard}>
                    <PerfTableChunk rows={chunk} styles={styles} />
                  </View>
                </Page>
              ))
            : []),
          <Page key={`${section.slug}-ytd`} size="LETTER" style={styles.page}>
            <PdfHeader header={headerProps} styles={styles} minimal />
            <Text style={[styles.subtle, { marginBottom: 10 }]}>{bundle.dateRangeLabel}</Text>
            <PdfSectionTitle styles={styles}>Year to Date</PdfSectionTitle>
            <View style={styles.surfaceCard}>
              <YtdTable
                rows={section.ytdRows}
                styles={styles}
                useEcommerceColumns={section.useMetaEcommerceColumns === true}
              />
            </View>
            {isLastSection ? footer : null}
          </Page>,
        ];
      })}
    </Document>
  );
}

function PdfHeader({
  header,
  styles,
  minimal,
}: {
  header: PdfHeaderProps;
  styles: ReturnType<typeof buildStyles>;
  /** Continuation pages: compact strip so each tab’s pages stay visually tied to the same section. */
  minimal?: boolean;
}) {
  if (minimal) {
    return (
      <View style={styles.headerMinimal}>
        <Text style={styles.viewTag}>{header.viewTitle}</Text>
        <Text style={styles.subtle}>{header.clientName}</Text>
      </View>
    );
  }
  return (
    <View style={styles.header}>
      {header.agencyLogoUrl ? (
        // @react-pdf/renderer's Image is not a DOM img — no alt prop in ImageProps (PDF output).
        // eslint-disable-next-line jsx-a11y/alt-text -- decorative agency mark in PDF header
        <Image src={header.agencyLogoUrl} style={styles.logo} />
      ) : (
        <View />
      )}
      <View style={styles.headerRight}>
        <Text style={styles.viewTag}>{header.viewTitle}</Text>
        <Text style={styles.clientName}>{header.clientName}</Text>
        <Text style={styles.subtle}>{header.dateRangeLabel}</Text>
      </View>
    </View>
  );
}

function formatKpiPdf(k: KpiStat, value: number): string {
  if (k.format === "currency") return formatCurrency(value);
  if (k.format === "percent") return `${value.toFixed(1)}%`;
  if (k.format === "multiplier") return `${value.toFixed(2)}x`;
  return formatWhole(value);
}

function HeroSection({
  hero,
  primaryColor,
  styles,
}: {
  hero: KpiStat[];
  primaryColor: string;
  styles: ReturnType<typeof buildStyles>;
}) {
  return (
    <View style={styles.kpiRow}>
      {hero.map((k) => {
        const deltaPct = k.prior > 0 ? ((k.current - k.prior) / k.prior) * 100 : 0;
        const favorable = k.lowerIsBetter ? deltaPct <= 0 : deltaPct >= 0;
        const arrow = deltaPct >= 0 ? "\u25B2" : "\u25BC";
        return (
          <View key={k.label} style={[styles.kpiCard, { borderTopColor: primaryColor }]}>
            <Text style={styles.kpiLabel}>{k.label}</Text>
            <Text style={styles.kpiValue}>{formatKpiPdf(k, k.current)}</Text>
            <Text style={styles.kpiPrior}>
              Prior:&nbsp;
              {formatKpiPdf(k, k.prior)}
            </Text>
            <Text style={favorable ? styles.kpiDeltaUp : styles.kpiDeltaDown}>
              {arrow + " "} {Math.abs(deltaPct).toFixed(1)}%
            </Text>
          </View>
        );
      })}
    </View>
  );
}

