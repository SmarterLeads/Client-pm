function conversionTypeBadgePdf(type: string): { bg: string; short: string } {
  const t = (type ?? "other").toLowerCase();
  if (t === "lead") return { bg: "#2563EB", short: "Lead" };
  if (t === "appointment") return { bg: "#9333EA", short: "Appt" };
  if (t === "call") return { bg: "#16A34A", short: "Call" };
  if (t === "chat") return { bg: "#EA580C", short: "Chat" };
  if (t === "purchase") return { bg: "#0891B2", short: "Buy" };
  return { bg: "#71717A", short: "Other" };
}

function deltaTone(
  current: number,
  prior: number,
  styles: ReturnType<typeof buildStyles>,
): { pctText: string; arrow: string; style: (typeof styles)[keyof typeof styles] } {
  if (prior > 0) {
    const pct = ((current - prior) / prior) * 100;
    const pctText = `${pct >= 0 ? "+" : ""}${pct.toFixed(1)}%`;
    if (Math.abs(pct) < 0.005) {
      return { pctText, arrow: "", style: styles.convDeltaNeutral };
    }
    if (pct > 0) return { pctText, arrow: "\u25B2 ", style: styles.convDeltaUp };
    return { pctText, arrow: "\u25BC ", style: styles.convDeltaDown };
  }
  if (prior === 0 && current > 0) {
    return { pctText: "New", arrow: "\u25B2 ", style: styles.convDeltaUp };
  }
  return { pctText: "0%", arrow: "", style: styles.convDeltaNeutral };
}

function formatPct(num: number, den: number): string {
  if (den <= 0) return "—";
  return `${((num / den) * 100).toFixed(2)}%`;
}

function formatCurrency(value: number): string {
  return value.toLocaleString("en-US", {
    style: "currency",
    currency: "USD",
    minimumFractionDigits: 2,
    maximumFractionDigits: 2,
  });
}

function formatWhole(value: number): string {
  return Math.round(value).toLocaleString("en-US");
}

function formatChartDateLabel(iso: string): string {
  const d = new Date(`${iso}T00:00:00.000Z`);
  if (Number.isNaN(d.getTime())) return iso;
  return d.toLocaleDateString("en-US", { month: "short", day: "numeric", timeZone: "UTC" });
}

export function chunkReportSeriesPoints(rows: ReportSeriesPoint[], maxPerPage: number): ReportSeriesPoint[][] {
  if (rows.length === 0) return [[]];
  const out: ReportSeriesPoint[][] = [];
  for (let i = 0; i < rows.length; i += maxPerPage) {
    out.push(rows.slice(i, i + maxPerPage));
  }
  return out;
}
