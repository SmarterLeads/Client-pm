function ConversionBreakdownCards({
  rows,
  styles,
  primaryColor,
}: {
  rows: ConversionBreakdownPdfRow[];
  styles: ReturnType<typeof buildStyles>;
  primaryColor: string;
}) {
  if (!rows?.length) return null;
  return (
    <View style={styles.cardGridFour}>
      {rows.map((r) => (
        <ConversionBreakdownPdfCard key={r.id} item={r} styles={styles} primaryColor={primaryColor} />
      ))}
    </View>
  );
}

function ConversionBreakdownPdfCard({
  item,
  styles,
  primaryColor,
}: {
  item: ConversionBreakdownPdfRow;
  styles: ReturnType<typeof buildStyles>;
  primaryColor: string;
}) {
  const badge = conversionTypeBadgePdf(item.conversionType);
  const delta = deltaTone(item.current, item.prior, styles);
  return (
    <View style={[styles.convCard, { borderTopColor: primaryColor }]}>
      <Text style={styles.convCardLabel}>{item.label}</Text>
      <View style={[styles.typeBadge, { backgroundColor: badge.bg }]}>
        <Text style={styles.typeBadgeText}>{badge.short}</Text>
      </View>
      <Text style={styles.convCardValue}>{formatWhole(Math.round(item.current))}</Text>
      <Text style={styles.convCardPrior}>
        Prior: {formatWhole(Math.round(item.prior))}
      </Text>
      <Text style={delta.style}>
        {delta.arrow}
        {delta.pctText}
      </Text>
    </View>
  );
}

